export function translate(lang: string, key: string, vars?: Record<string,string>): string {
  const dict = T[lang] ?? T["en"];
  let str = dict[key] ?? T["en"][key] ?? key;
  if (vars) Object.entries(vars).forEach(([k, v]) => { str = str.replace(`{${k}}`, v); });
  return str;
}

